public class A1103307_0224_1 {
    public static void main(String[] args){
        Scanner input = new Scanner(System.in);

        System.out.print("請輸入一個整數:");
        int num = input.nextInt();
       
        if (num % 2 == 0) {
            System.out.println("偶數");
        } else {
            System.out.println("奇數");
        }

        
    }


    
}
